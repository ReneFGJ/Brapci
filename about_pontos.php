<h1>Metodologia de Pontos para Recuperação da Informação - Pontos</h1>
</A>

<div style="text-align: justify">
A metodologia de atribuição de pesos define
características de uma metodologia de RI para determinar a relevância de
estudos em uma área temática específica. 
A metodologia compara a incidência dos termos nos campos título, 
palavra-chave e resumo, de forma a atribuir peso (estrelas) 
conforme a incidências dos termos de busca nos campos selecionados.

