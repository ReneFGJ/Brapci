<B>Sobre o projeto</B>
<BR><BR>
O Editor Brapci Skos (EBSkos) é uma ferramenta que possibilita a criação e desenvolvimento de tesauros que utiliza
o modelo Simple Knowledge Organize System (SKOS) para organização das informações.
<BR><BR>
Este modelo permite a criação de tesauros dinâmicos e seu compartilhamento com outras instruições por utilizar de um
protocolo de comunicação aberto.
<BR><BR>
O gestor do SKOS é a W3C, disponibilizando as normas para construção e compartilhamento de tesauros SKOS e ferramentas de validação<sup>1</sup> com o objetivo de padronizar sua compartibilidade com outras bases.

