<h1>Ajuda sobre as buscas</h1>
<UL>
	<h2>Busca simples</h2>
	<p>
		A busca simples é realizada utilizando apenas um termo, o resultado é apresentado conforme a incidência nos trabalhos disponíveis.
	</p>
	</br></br>
	
	<h2>Busca com dois ou mais termos</h2>
	<P>
		A busca utilizando uma palavra ou frase entre aspas resulta apenas trabalhos com as mesmas palavras do que está dentro das aspas. 
		Use isso apenas se você estiver procurando por uma palavra ou frase exata. Caso contrário, você excluirá muitos resultados úteis.
		Exemplo: "Ciência da Informação"
	</P>
	</br></br>
	
